export function translateText(value: string, locale: Locale): string {
  if (locale === "en") return value;
  const exact = japanese[value];
  if (exact) return exact;
  for (const [pattern, render] of japanesePatterns) {
    const match = value.match(pattern);
    if (match) return render(...match.slice(1));
  }
  return value;
}

export function resolveLocale(saved: string | null, languages: readonly string[]): Locale {
  if (saved === "ja" || saved === "en") return saved;
  return languages.some((language) => language.toLowerCase().startsWith("ja")) ? "ja" : defaultLocale;
}

export function resolveInitialLocale(): Locale {
  if (typeof window === "undefined") return defaultLocale;
  return resolveLocale(window.localStorage.getItem(localeStorageKey), window.navigator.languages);
}
